package SecondWithProgrammingInScala

/**
  * 类 和 对象
  * 本章主要是描述scala中类和对象的建立
  * 直接通过代码体现
  *
  * scala中将类/对象做了区分
  * 类就是纯粹的类 ,也就是封装完整的"对象" ,他的方法应该是面向对象的 ,而不会出现静态的属性和方法(静态是公用的/全局的)
  * 对象则代表原来的单例对象 ,里面的属性和方法都是静态的(类似于java的static)
  *
  * 使用class需要通过 new 实例化
  * 使用object则可以直接调用(静态代码段/静态方法)
  *
  * 非脚本的scala文件需要通过
  * $ scalac 编译
  * $ fsc 快速变异
  */

